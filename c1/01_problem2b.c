#include<stdio.h>

int main()
{
    int radius=3;
    float height=4.5;

    printf("The area of the circle is %f.\n", 3.14*radius*radius);
    printf("The volume of the cylinder is %f.\n", 3.14*radius*radius*height);

    return 0;

}